function setTxtAny(id, txt){
  const el = byId(id);
  if (el && typeof txt === "string") el.textContent = txt;
}
function setHTMLAny(id, html){
  const el = byId(id);
  if (el && typeof html === "string") el.innerHTML = html;
}

